public class JavaBasics{
    public static void main(String args[]){
        System.out.println("Here we go:)"); //println gives line space
        System.out.print("hello world!\nhello world!\n");  // \n also gives result on next line
        System.out.print("1\n2\n3");

        int a = 10; //constant value called as literal(9)
        int b = 5;
        String name = "sakshi";
        System.out.println(a);
        System.out.println(b);
        System.out.println(name);
  

        a = 9;     //we can channge the variable value whenever we want to
        System.out.println(a);
        a=b;       //we can assign var b value to var a
        System.out.print(a);
    }   
} 


